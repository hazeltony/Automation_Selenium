package loops;

public class ForLoop { //for loop for reading the excel library which had row count and column count
	//one loop for rows and another for columns

	public static void main(String[] args) {

		// initialization //Condition //increment/decrement
       // for(int i=0;i>=10;i++) to print 0-10
		
		for (int i = 10; i >= 0; i--) {
			System.out.println(i); // to print 10-0

		}
	}

}
